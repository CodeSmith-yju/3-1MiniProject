using System.Collections;
using System.Linq;
using UnityEngine;
using UnityEngine.AI;

public class BaseEntity : MonoBehaviour
{
    public float max_Hp;
    public float cur_Hp;
    public float max_Mp;
    public float cur_Mp;
    public float atkDmg;
    public float atkSpd;
    public float atkRange;
    public bool isAttack = false;
    public bool isAtkDone = false;
    public bool able_Skill = false;

    NavMeshAgent agent;

    

    // 플레이어, 적 오브젝트의 어떤 행동을 하는지 설정
    public enum State
    {
        Idle,
        Move,
        Attack,
        Skill,
        Death
    }

    public State _curstate;
    protected StateManager _stateManager;

    private void Awake()
    {
        agent = GetComponent<NavMeshAgent>();
        agent.updateRotation = false;
        agent.updateUpAxis = false;
    }


    protected virtual void Start()
    {
        _curstate = State.Idle;
        _stateManager = new StateManager(new IdleState(this));

        max_Hp = 10f;
        cur_Hp = max_Hp;
        max_Mp = 5f;
        cur_Mp = max_Mp;
        atkDmg = 1f;
        atkRange = 1f;
        atkSpd = 1f;
        able_Skill = false;
    }

    // 배틀 시작 시 오토 배틀 로직
    protected virtual void Update()
    {
        if (BattleManager.Instance._curphase == BattleManager.BattlePhase.Battle)
        {
            switch (_curstate)
            {
                case State.Idle:
                    if (FindTarget() != null)
                    {
                        if (IsAttack(atkRange))
                        {
                            ChangeState(State.Attack);
                        }
                        else
                        {
                            ChangeState(State.Move);
                        }
                    }
                    break;

                case State.Move:
                    if (FindTarget() != null)
                    {
                        if (IsAttack(atkRange))
                        {
                            ChangeState(State.Attack);
                        }
                    }
                    else
                    {
                        ChangeState(State.Idle);
                    }
                    break;

                case State.Attack:
                    if (FindTarget() != null)
                    {
                        if (!IsAttack(atkRange))
                        {
                            ChangeState(State.Move);
                        }

                        if (isAtkDone)
                        {
                            Debug.Log("공격 완료 - Idle로 상태 변경 (새로운 타겟 지정)");
                            isAtkDone = false;
                            ChangeState(State.Idle);
                        }
                    }
                    else
                    {
                        Debug.Log("타겟 없음");
                        isAtkDone = false;
                        ChangeState(State.Idle);
                    }
                    break;
                case State.Death:
                    Destroy(gameObject);
                    break;
            }

            _stateManager.UpdateState();

            // 현재 체력이 0이 되면 Death 상태로 변하고 상태창도 죽은 것으로 표시
            if (cur_Hp <= 0)
            {
                _curstate = State.Death;
            }

            if (able_Skill)
            {
                if (cur_Mp == max_Mp && cur_Mp != 0)
                {
                    _curstate = State.Skill;
                }
            }
            

        }

        /*if (gameObject != null)
        {
            if (transform.localScale.x < 0)
            {
                transform.localScale = new Vector3(-0.75f, 0.75f, 1f);
            }
            if (transform.localScale.x > 0)
            {
                transform.localScale = new Vector3(0.75f, 0.75f, 1f);
            }
        }*/
    }

    // 상태 변환 메서드
    protected void ChangeState(State newState)
    {
        _curstate = newState;

        switch (_curstate)
        {
            case State.Idle:
                _stateManager.ChangeState(new IdleState(this));
                break;
            case State.Move:
                _stateManager.ChangeState(new MoveState(this));
                break;
            case State.Attack:
                _stateManager.ChangeState(new AttackState(this));
                break;
        }
    }




    // 가까이에 있는 적을 타겟하는 메소드

    public GameObject FindTarget()
    {
        string targetTag = (tag == "Player") ? "Enemy" : "Player";
        // 타겟 오브젝트 배열 찾기
        GameObject[] targets = GameObject.FindGameObjectsWithTag(targetTag);
        // 현재 위치
        Vector3 currentPosition = transform.position;
        // 가장 가까운 대상 찾기
        GameObject nearestTarget = targets.OrderBy(target => Vector3.Distance(currentPosition, target.transform.position)).FirstOrDefault();
        // 찾은 대상 반환
        return nearestTarget != null ? nearestTarget : null;
    }


    // 1초마다 타겟을 업데이트 하는 메소드
    public IEnumerator UpdateTarget()
    {
        if (FindTarget() != null) 
        {
            while (true)
            {
                yield return new WaitForSeconds(0.5f); // 1초 대기
                FindTarget();
            }
        }
        
    }

    // Idle 상태이거나 Attack 상태일때 최대한 피할수 있게 우선순위 높히는 메서드 ( NavMeshPlus 에셋 관련 )
    public void SetMovementPriority(bool isMoving)
    {
        int priority = isMoving ? 50 : 30; // 이동 중이면 우선순위를 50으로, 아니면 30로 설정
        agent.avoidancePriority = priority;
    }

    
    // 타겟으로 향해 이동하는 메서드 ( NavMeshPlus 이용 )
    public void MoveToTarget()
    {
        Transform target = FindTarget().transform;
        if(target != null) 
        {
            agent.isStopped = false;
            agent.SetDestination(target.position);
            SetMovementPriority(true);
        }
        else
        {
            return;
        }
        
    }

    // 공격 사거리에 들어오면 이동 멈추고 공격 준비
    public void StopMove()
    {
        if (isAttack)
        {
            agent.isStopped = true;
            SetMovementPriority(false);
        }
    }


    // 공격 사거리에 오면 논리형으로 True or False 반환하는 메서드
    public bool IsAttack(float range)
    {
       
        Transform target = FindTarget().transform;

        Vector2 tVec = (Vector2)(target.localPosition - transform.position);
        float tDis = tVec.sqrMagnitude;

        if (tDis <= atkRange * atkRange)
        {
            isAttack = true;
        }
        else
        {
            isAttack = false;
        }

        return isAttack;
    }

    public IEnumerator Attack()
    {
        if (_curstate == State.Attack)
        {
            BaseEntity target = FindTarget()?.GetComponent<BaseEntity>();

            if (target != null && isAttack)
            {
                while (true)
                {
                    yield return new WaitForSeconds(atkSpd);

                    if (target == null || target.cur_Hp <= 0)
                    {
                        // 공격 중지
                        isAtkDone = true;
                        break;
                    }
                    Debug.Log("공격함");
                    if (able_Skill)
                    {
                        cur_Mp++;
                    }

                    float getDmgHp = target.cur_Hp - atkDmg;
                    target.cur_Hp = getDmgHp;
                    Debug.Log(target.cur_Hp + " " + target.name);
                }
            }
        }
    }
}
